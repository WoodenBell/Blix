package io.github.virtualiceshard.blix;

public class ArrayBoxing {
	public static class IntArray {
		int[] array;
		public IntArray(int[] arr) {
			array = arr;
		}
	}
	public static class CharArray {
		char[] array;
		public CharArray(char[] arr) {
			array = arr;
		}
	}
	public static class ByteArray {
		char[] array;
		public ByteArray(char[] arr) {
			array = arr;
		}
	}
}
